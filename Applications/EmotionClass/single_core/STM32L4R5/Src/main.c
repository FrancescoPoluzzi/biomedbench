// STOP Mode 2
void deep_sleep(){
		  // Prepare sleep mode
			HAL_SuspendTick();

		  // Shut down FLASH memory
		  __HAL_FLASH_SLEEP_POWERDOWN_ENABLE();

		  /* Enter Sleep mode */
		  HAL_PWREx_EnterSTOP2Mode(PWR_STOPENTRY_WFI);

		  // Recover from sleep
		  SystemClock_Config();
		  HAL_ResumeTick();
}


// Light sleep
// Allows DMA to function
void light_sleep()	{
	 	// Sleep and wait for DMA to complete
		HAL_SuspendTick();
		while (dma_working)
			HAL_PWR_EnterSLEEPMode(PWR_MAINREGULATOR_ON, PWR_STOPENTRY_WFI);

		SystemClock_Config();
		HAL_ResumeTick();
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
}


// Receive input signal through SPI
void fetch_input_SPI(uint8_t *Recv_Buffer, uint16_t buffer_size)	{
	// receive through DMA
	dma_working = 1;
	// Manually enable Chip select
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
	HAL_SPI_Receive_DMA(&hspi1, Recv_Buffer, buffer_size);
}

#ifdef DATA_ACQUISITION
// GPIO callback from wakeup interrupt
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)	{
	// Recover from sleep
	SystemClock_Config();
	HAL_ResumeTick();

	fetch_input_SPI((uint8_t *) (input_array + no_wakeups * BUFFER_SIZE), (uint16_t) BUFFER_SIZE);

	no_wakeups++;
	callback_flag = 1;
}

// Convert 24-bit samples to float
// assume 12-bit decimal part
void prepare_input()	{
	int j = 0;
	for (int i = 0; i < NO_BATCHES * BUFFER_SIZE; i+=3)	{
		int32_t *ptr = (int32_t *)&input_array[i];

		// positive number
		if (input_array[i] > 0)	{
			int32_t temp = 0x00ffffff & (*ptr);
			emg_data[j++] = (float) temp / (float) 4096;
		}
		// negative number
		else	{
			int32_t temp = (0x80000000) | (0x00ffffff & (*ptr));
			// 2's complement
			temp--;
			temp = ~temp;
			emg_data[j] = (float) temp / (float) 4096;
			emg_data[j++] = -emg_data[j++];
		}
	}
}
#endif

